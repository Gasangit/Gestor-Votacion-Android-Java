package com.example.acn4av_mansilla_angel_gaston_tp2;

public class OpcionesObjeto {

    private String id;
    private String description;

    public OpcionesObjeto(String id, String description) {
        this.id = id;
        this.description = description;
    }

    public String getId() {
        return id;
    }
    public String getDescription() {
        return description;
    }
}
